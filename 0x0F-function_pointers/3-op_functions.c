#include "3-calc.h"

/**
 * op_add - adds two intergers
 * Prototype: int op_add(int a, int b);
 * @a: integer
 * @b: integer
 *
 * Return: the sum of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_add - adds two intergers
 * Prototype: int op_add(int a, int b);
 * @a: integer
 * @b: integer
 *
 * Return: the result of the sum of two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_sub - substracts two intergers
 * Prototype: int op_add(int a, int b);
 * @a: integer
 * @b: integer
 *
 * Return: the result of the substraction of two integers
 */
int op_mul(int a, int b)
{
return (a * b);
}


/**
 * op_mul - multiplies two intergers
 * Prototype: int op_add(int a, int b);
 * @a: integer
 * @b: integer
 *
 * Return: the result of the multication of two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_add - adds two intergers
 * Prototype: int op_add(int a, int b);
 * @a: integer
 * @b: integer
 *
 * Return: the result of the modulo of two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
